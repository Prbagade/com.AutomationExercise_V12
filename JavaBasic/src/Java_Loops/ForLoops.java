package Java_Loops;

public class ForLoops {

	
	public static void main(String[] args) {
		
		//for loops
		
//		print 1 to 10

		
		for(int s=1;s<=10;s++) 
		{
			System.out.println(s);
		}
		
		System.out.println("*****************************");
		
//		print 10 to 1
		
		for(int i=10;i>=1;i--) 
		{
			System.out.println(i);
		}
		
	}
}
